#include "sdk.h"
	
void evmboardinit()
{
	SYSCON_REG_WRITE(SYSCON_WREN_BIT_PINSEL, R_SYSCON_PSR, 1<<0); // 64pin 
		
	*R_PAF(0) = F_PAF0_0_TIMER0_PWM | F_PAF0_1_GP1 | F_PAF0_2_GP2 | F_PAF0_3_UART3_RX | F_PAF0_4_UART3_TX | F_PAF0_5_TWI0_SDA | F_PAF0_6_TWI0_SCL | F_PAF0_7_I2S_LRCLK;
	*R_PAF(1) = F_PAF1_0_SPI0_CS | F_PAF1_1_SF_DQ1 | F_PAF1_2_SF_DQ2 | F_PAF1_3_SF_DQ0 | F_PAF1_4_SF_CLK  | F_PAF1_5_SF_DQ3 | F_PAF1_6_SPI1_CS | F_PAF1_7_SPI1_SDO;
	*R_PAF(2) = F_PAF2_0_SPI1_SDI | F_PAF2_1_SPI1_CLK | F_PAF2_2_UART5_RX | F_PAF2_3_UART5_TX;	
	*R_PAF(4) = F_PAF4_0_SPWM_N0 | F_PAF4_1_SPWM_P0 | F_PAF4_2_SPWM_N1 | F_PAF4_3_SPWM_P1;	
}
